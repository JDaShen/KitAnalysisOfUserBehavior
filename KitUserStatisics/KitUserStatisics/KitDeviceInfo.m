- (NSString *)uuid {
    NSMutableDictionary *uuidKVPairs = (NSMutableDictionary *)[self load:KEY_KEYCHAIN_UUID];
    NSString *uuid = [uuidKVPairs objectForKey:KEY_UUID];
    if (uuid == nil || uuid.length == 0) {
        // 获取UUID
        uuid = [[UIDevice currentDevice].identifierForVendor UUIDString];
        // 保存UUID到keychain
        uuidKVPairs = [[NSMutableDictionary alloc] init];
        [uuidKVPairs setObject:uuid forKey:KEY_UUID];
        [self save:KEY_KEYCHAIN_UUID data:uuidKVPairs];
    }
    return uuid;
}

